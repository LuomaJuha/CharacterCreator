package charactercreator;

/**
 *
 * @author Suolapurkki
 */
public class Character {
    private String name;
    private String age;
    private String occupation;
    private String parentOccupation;
    private String culture;
    private String clan;
    
    private int strength;
    private int constitution;
    private int size;
    private int wisdom;
    private int power;
    private int dexterity;
    private int appereance;
    
    public Character(){
        super();
    }
    
    //Getter and Setter methods
    // <editor-fold defaultstate="collapsed" desc="user-description">
    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the age
     */
    public String getAge() {
        return age;
    }

    /**
     * @param age the age to set
     */
    public void setAge(String age) {
        this.age = age;
    }

    /**
     * @return the occupation
     */
    public String getOccupation() {
        return occupation;
    }

    /**
     * @param occupation the occupation to set
     */
    public void setOccupation(String occupation) {
        this.occupation = occupation;
    }

    /**
     * @return the parentOccupation
     */
    public String getParentOccupation() {
        return parentOccupation;
    }

    /**
     * @param parentOccupation the parentOccupation to set
     */
    public void setParentOccupation(String parentOccupation) {
        this.parentOccupation = parentOccupation;
    }

    /**
     * @return the culture
     */
    public String getCulture() {
        return culture;
    }

    /**
     * @param culture the culture to set
     */
    public void setCulture(String culture) {
        this.culture = culture;
    }

    /**
     * @return the clan
     */
    public String getClan() {
        return clan;
    }

    /**
     * @param clan the clan to set
     */
    public void setClan(String clan) {
        this.clan = clan;
    }

    /**
     * @return the strength
     */
    public int getStrength() {
        return strength;
    }

    /**
     * @param strength the strength to set
     */
    public void setStrength(int strength) {
        this.strength = strength;
    }

    /**
     * @return the constitution
     */
    public int getConstitution() {
        return constitution;
    }

    /**
     * @param constitution the constitution to set
     */
    public void setConstitution(int constitution) {
        this.constitution = constitution;
    }

    /**
     * @return the size
     */
    public int getSize() {
        return size;
    }

    /**
     * @param size the size to set
     */
    public void setSize(int size) {
        this.size = size;
    }

    /**
     * @return the wisdom
     */
    public int getWisdom() {
        return wisdom;
    }

    /**
     * @param wisdom the wisdom to set
     */
    public void setWisdom(int wisdom) {
        this.wisdom = wisdom;
    }

    /**
     * @return the power
     */
    public int getPower() {
        return power;
    }

    /**
     * @param power the power to set
     */
    public void setPower(int power) {
        this.power = power;
    }

    /**
     * @return the dexterity
     */
    public int getDexterity() {
        return dexterity;
    }

    /**
     * @param dexterity the dexterity to set
     */
    public void setDexterity(int dexterity) {
        this.dexterity = dexterity;
    }

    /**
     * @return the appereance
     */
    public int getAppereance() {
        return appereance;
    }

    /**
     * @param appereance the appereance to set
     */
    public void setAppereance(int appereance) {
        this.appereance = appereance;
    }
    
    // </editor-fold>
    
}
